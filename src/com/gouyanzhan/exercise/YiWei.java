package com.gouyanzhan.exercise;

public class YiWei {
    public static void main(String[] args) {
        printShift();
    }

    /**
     * 移位运算
     */
    private static void printShift() {
        int i = 6 << 1;
        System.out.println("6左移1位的值等于" + i);
        int a = 7 >> 1;
        System.out.println("7右移1位的值等于" + a);
        int b = -8 >>> 1;
        System.out.println("-8无符号右移1位的值等于" + b);

    }
}
