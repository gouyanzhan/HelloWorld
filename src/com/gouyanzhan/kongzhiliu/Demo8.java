package com.gouyanzhan.kongzhiliu;

public class Demo8 {
    public static void main(String[] args) {
        forLoop();
    }

    private static void forLoop(){
        //使用多个int变量来控制for循环
        for (int n =20,i=0;i<n;i++,n--) {
            System.out.println("n= " + n + " i= " + i);
        }
    }
}
