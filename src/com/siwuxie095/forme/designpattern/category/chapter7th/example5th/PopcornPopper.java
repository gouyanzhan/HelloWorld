/**
 * 爆米花机
 *
 * @author Jiajing Li
 * @date 2019-11-04 20:25:42
 */
class PopcornPopper {

    void on() {
        System.out.println("爆米花机打开");
    }

    void off() {
        System.out.println("爆米花机关闭");
    }

    void pop() {
        System.out.println("爆米花机开始爆米花");
    }

}
