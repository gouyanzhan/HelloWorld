/**
 * 灯（接收者）
 *
 * @author Jiajing Li
 * @date 2019-10-29 17:04:12
 */
class Light {

    /**
     * 打开
     */
    void on() {
        System.out.println("将灯打开");
    }

    /**
     * 关闭
     */
    void off() {
        System.out.println("将灯关闭");
    }

}

