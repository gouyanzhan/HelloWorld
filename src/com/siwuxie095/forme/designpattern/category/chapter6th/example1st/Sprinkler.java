/**
 * 洒水器
 *
 * @author Jiajing Li
 * @date 2019-10-27 16:57:55
 */
class Sprinkler {

    /**
     * 打开水源
     */
    void waterOn() {
        System.out.println("将洒水器的水源打开");
    }

    /**
     * 关闭水源
     */
    void waterOff() {
        System.out.println("将洒水器的水源关闭");
    }

}
