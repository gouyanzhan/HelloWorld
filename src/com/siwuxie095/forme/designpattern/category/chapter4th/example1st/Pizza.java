/**
 * 比萨
 *
 * @author Jiajing Li
 * @date 2019-10-11 17:42:56
 */
abstract class Pizza {

    /**
     * 准备
     */
    abstract void prepare();

    /**
     * 烘烤
     */
    abstract void bake();

    /**
     * 切片
     */
    abstract void cut();

    /**
     * 装盒
     */
    abstract void box();

}
